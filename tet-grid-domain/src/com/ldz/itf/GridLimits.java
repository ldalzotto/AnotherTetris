package com.ldz.itf;

public class GridLimits {

    private RectangleLimit bottomLimit;
    private RectangleLimit leftLimit;
    private RectangleLimit rightLimit;

    public RectangleLimit getBottomLimit() {
        return bottomLimit;
    }

    public void setBottomLimit(RectangleLimit bottomLimit) {
        this.bottomLimit = bottomLimit;
    }

    public RectangleLimit getLeftLimit() {
        return leftLimit;
    }

    public void setLeftLimit(RectangleLimit leftLimit) {
        this.leftLimit = leftLimit;
    }

    public RectangleLimit getRightLimit() {
        return rightLimit;
    }

    public void setRightLimit(RectangleLimit rightLimit) {
        this.rightLimit = rightLimit;
    }

}
